static const int kNumPatches = (int)(sizeof(kMoroPatches) / sizeof(kMoroPatches[0]));
static unsigned char g_vanilla[kNumPatches][285]; // original bytes, captured at startup
static bool g_inited = false;
static bool g_applied = false;

// Optional registry bitmask "MoroPatchMask" (hex, default all-on) to disable individual regions while
// testing. Bit i = region i above.
static unsigned int readMask()
{
  char buf[16] = {};
  DWORD sz = sizeof(buf);
  if (RegGetValueA(HKEY_CURRENT_USER, "Software\\WestwoodRedirect\\Emperor\\LauncherCustomSettings",
        "MoroPatchMask", RRF_RT_REG_SZ, nullptr, buf, &sz) == ERROR_SUCCESS)
    return (unsigned int)strtoul(buf, nullptr, 16);
  return 0xFFFFu;
}

void initHudFix()
{
  for (int i = 0; i < kNumPatches; i++)
  {
    const MoroPatch& p = kMoroPatches[i];
    DWORD oldProt = 0;
    if (!VirtualProtect((void*)p.va, p.len, PAGE_EXECUTE_READWRITE, &oldProt))
    {
      Log("HUD: VirtualProtect failed at %08X (err %lu)\n", p.va, GetLastError());
      continue;
    }
    memcpy(g_vanilla[i], (void*)p.va, p.len); // Game.exe is still vanilla here
  }
  g_inited = true;
  g_applied = false;
  Log("HUD: init done, vanilla bytes captured for %d regions (patch starts OFF)\n", kNumPatches);
}

void hudFixSetState(bool apply)
{
  if (!g_inited || apply == g_applied)
    return;
  unsigned int mask = readMask();
  for (int i = 0; i < kNumPatches; i++)
  {
    const MoroPatch& p = kMoroPatches[i];
    const void* src = apply && ((mask >> i) & 1u) ? (const void*)p.bytes : (const void*)g_vanilla[i];
    memcpy((void*)p.va, src, p.len);
    FlushInstructionCache(GetCurrentProcess(), (void*)p.va, p.len);
  }
  if (apply)
  {
    // Moro's cave normally computes logicalW (0x65fff8 = realH*4/3) on a height write, which only
    // happens at startup - before we toggle the patch on. Compute it directly so the 2D UI redirects
    // have a valid width the moment the patch goes live mid-mission (otherwise the HUD reads 0 and
    // collapses to the screen edge). 0x65fffc = 1.3f is the cave's scroll-extent constant.
    int realH = *(int*)0x0060245c;
    if (realH > 0)
    {
      *(DWORD*)0x0065fff8 = (DWORD)(realH * 4 / 3);
      *(DWORD*)0x0065fffc = 0x3fa66666; // 1.3f
    }
  }
  g_applied = apply;
  Log("HUD: patch %s (mask %04X)\n", apply ? "APPLIED (in mission)" : "REMOVED (front-end menu)", mask);
}
